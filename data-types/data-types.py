####
### Boolean Types

# Bool
a = True

####
### Numeric Types

# Integer
a = 10

# Float
a = 10.5

# Complex
a = 1j

####
### Text Types

# String
a = "This is string"

####
### Sequence Types

# List
a = [1, 2, 3]

# Tuple - immutable list
a = (1, 2, 3)

# Range
a = range(6)

####
### Mapping Types

# Dictionary
a = {
  "name" : "John",
  "age" : 36
}

####
### Set Types

# Set
a = {"apple", "banana", "pear"}

# Frozen Set
a = frozenset({"apple", "banana", "pear"})

####
### Binary Types

# Bytes
a = b"Hello"

# Bytes Array
a = bytearray(5)

# Memory View
a = memoryview(bytes(5))

####
### None Types

# No Type
a = None