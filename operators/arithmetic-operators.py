x = 5
y = 4

# Addition operator
z = x + y

print(f"{x} + {y} = {z}")

# Subtraction operator
z = x - y

print(f"{x} - {y} = {z}")

# Multiplication operator
z = x * y

print(f"{x} * {y} = {z}")

# Division operator
z = x / y

print(f"{x} / {y} = {z}")

# Floor operator
z = x // y

print(f"{x} // {y} = {z}")

# Modulus operator
z = x % y

print(f"{x} % {y} = {z}")

# Power operator
z = x ** y

print(f"{x} ** {y} = {z}")